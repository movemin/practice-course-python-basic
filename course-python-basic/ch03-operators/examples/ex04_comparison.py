# 비교 연산자 (Comparison Operators)
# 두 값을 비교하여 True 또는 False를 반환한다

a = 10
b = 5

print(a == b)   # 같은가? False
print(a != b)   # 다른가? True
print(a > b)    # 큰가? True
print(a < b)    # 작은가? False
print(a >= b)   # 크거나 같은가? True
print(a <= b)   # 작거나 같은가? False

# 문자열 비교도 가능하다
print("abc" == "abc")  # True
print("abc" == "ABC")  # False (대소문자 구분)
