# 복합 대입 연산자 (Compound Assignment Operators)
# 연산과 대입을 한 번에 처리한다

score = 100

score += 10   # score = score + 10
print(score)  # 110

score -= 20   # score = score - 20
print(score)  # 90

score *= 2    # score = score * 2
print(score)  # 180

score //= 3   # score = score // 3
print(score)  # 60

# 문자열에도 사용 가능
message = "파이썬"
message += " 화이팅!"
print(message)  # 파이썬 화이팅!
