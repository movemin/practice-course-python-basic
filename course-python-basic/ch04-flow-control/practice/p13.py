"""
실습 13: 미니 성적 관리

5명의 점수를 입력받아 평균과 최고점을 출력하세요.

[조건]
- 리스트를 사용하지 않고 변수만으로 해결합니다.
- 합계와 최고점을 누적하며 계산합니다.

힌트: for + if + 누적 변수를 사용합니다.
"""

total = 0
best = 0

# 아래에 5명의 점수를 입력받아 평균과 최고점을 출력하세요
# for 문으로 1번부터 5번까지의 학생의 점수를 입력할 수 있도록 한다.
for i in range(1, 6) :
    score = int(input(f"{i}번 학생 점수: "))
# 1번 학생이 입력했을 경우 먼저 total에 입력 수를 더한다
    total += score
# 1번부터 5번까지의 학생 입력 수부터 가장 큰 숫자가 나올 경우 변수선언이 일어나지 않고 덮어 쓰이는 것을 이용한다
    if score > best :
        best = score
# 평균과 최고점을 출력한다
print(f"평균: {total/5}")
print(f"최고점: {best}")
