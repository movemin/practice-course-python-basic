# 흐름제어(Flow Control)란?
#
# 프로그램은 기본적으로 위에서 아래로 한 줄씩 실행된다.
# 흐름제어를 사용하면 실행 순서를 바꿀 수 있다.
#
# 3가지 흐름:
# 1. 순차 — 위에서 아래로 차례대로
# 2. 선택 — 조건에 따라 다른 코드 실행
# 3. 반복 — 같은 코드를 여러 번 실행

# --- 1. 순차 ---
print("아침에 일어난다")
print("학교에 간다")
print("공부를 한다")

print()

# --- 2. 선택 ---
weather = "비"

if weather == "비":
    print("우산을 가져간다")
else:
    print("그냥 나간다")

print()

# --- 3. 반복 ---
for i in range(3):
    print("복습한다")
