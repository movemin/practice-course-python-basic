"""
실습 12 풀이: 숫자 피라미드
"""

n = int(input("줄 수를 입력하세요: "))

# 바깥 for문: 줄 번호 (1줄, 2줄, 3줄, ...)
for i in range(1, n + 1):
    # 안쪽 for문: 해당 줄에 1부터 i까지 숫자 출력
    # 예: 3번째 줄이면 j가 1, 2, 3으로 바뀌며 "123" 출력
    for j in range(1, i + 1):
        print(j, end="")  # 줄바꿈 없이 옆으로 이어서 출력
    print()  # 한 줄 끝나면 줄바꿈
