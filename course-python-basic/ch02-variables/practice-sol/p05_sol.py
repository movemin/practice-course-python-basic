"""
실습 5 풀이: 자료형에 따른 연산 차이 확인하기
"""

# [1] + 연산자: 자료형에 따라 의미가 다르다
print(10 + 20)       # 숫자 + 숫자 → 덧셈 → 30
print("10" + "20")   # 문자열 + 문자열 → 이어붙이기 → "1020"

# A1: 같은 + 연산자지만 숫자는 덧셈, 문자열은 이어붙이기를 한다


# [2] * 연산자: 자료형에 따라 의미가 다르다
print(3 * 4)         # 숫자 * 숫자 → 곱셈 → 12
print("하" * 4)      # 문자열 * 숫자 → 반복 → "하하하하"

# A2: 같은 * 연산자지만 숫자는 곱셈, 문자열은 반복을 한다


# [3] 서로 다른 자료형끼리 + 연산은 오류가 발생한다
# print("점수: " + 100)  # TypeError 발생!

# A3: str()로 숫자를 문자열로 변환하면 이어붙이기가 가능하다
print("점수: " + str(100))


# [4] type()으로 자료형을 확인하면 오류 원인을 파악할 수 있다
a = "100"   # 따옴표가 있으므로 문자열
b = 100     # 따옴표가 없으므로 정수

print(type(a))  # <class 'str'>
print(type(b))  # <class 'int'>

# A4: a는 str, b는 int → 서로 다른 자료형이라 + 연산 시 TypeError 발생
