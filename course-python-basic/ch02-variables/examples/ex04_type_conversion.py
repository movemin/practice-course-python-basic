# 자료형 변환 (Type Conversion)
# 서로 다른 자료형끼리는 바로 연산할 수 없는 경우가 있습니다.
# 원하는 연산을 하려면 자료형을 맞춰야 합니다.
# 이때 자료형 변환을 사용합니다.

# ----------------------------------------
# [1] 왜 자료형 변환이 필요한가?
# ----------------------------------------

# 숫자처럼 보이지만 문자열이기 때문에 계산이 안 됩니다.

price = "3000"
quantity = "2"

print(price + quantity)       # "30002" (이어붙이기 - 의도한 결과가 아님!)
# print(price * quantity)     # 오류! str * str은 불가능

# 계산을 하려면 숫자로 변환해야 합니다.
price = int("3000")
quantity = int("2")

print(price * quantity)       # 6000 (곱셈 - 올바른 결과)

# input()으로 입력받은 값도 항상 문자열(str)이므로 변환이 필요합니다.
num = input("숫자를 입력하세요: ")
print(type(num))              # <class 'str'>
print(num + num)              # 이어붙이기 (예: "55")

num = int(num)
print(type(num))              # <class 'int'>
print(num + num)              # 덧셈 (예: 10)

# ----------------------------------------
# [2] 자료형 변환 함수
# ----------------------------------------

# int() - 정수로 변환
print(int("25"))        # 문자열 → 정수
print(int(3.14))        # 실수 → 정수 (소수점 버림)

# float() - 실수로 변환
print(float("3.14"))    # 문자열 → 실수
print(float(10))        # 정수 → 실수

# str() - 문자열로 변환
print(str(100))         # 정수 → 문자열
print(str(3.14))        # 실수 → 문자열

# ----------------------------------------
# [3] 실전 예시: 문자열과 숫자를 함께 출력하기
# ----------------------------------------

name = "홍길동"
score = 95

# 방법 1: str()로 변환하여 + 연결
print("이름: " + name + ", 점수: " + str(score))

# 방법 2: 콤마(,)로 구분 (자동으로 띄어쓰기 추가)
print("이름:", name, ", 점수:", score)

