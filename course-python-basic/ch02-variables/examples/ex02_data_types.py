# 기본 자료형 (Data Types)
# 변수에는 서로 다른 종류의 값을 저장할 수 있습니다.
# 저장되는 값의 종류를 자료형(Data Type)이라고 합니다.
# 파이썬의 변수는 저장하는 값에 따라 자료형이 자동으로 결정됩니다.
# type() 함수를 사용하면 변수의 자료형을 확인할 수 있습니다.

# 문자열 (str) - 텍스트 데이터
name = "홍길동"
print(name, type(name))

# 정수 (int) - 소수점이 없는 숫자
age = 20
print(age, type(age))

# 실수 (float) - 소수점이 있는 숫자
height = 175.5
print(height, type(height))

# 불리언 (bool) - 참(True) 또는 거짓(False)
is_student = True
print(is_student, type(is_student))
