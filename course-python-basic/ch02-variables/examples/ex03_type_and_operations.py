# 자료형과 연산 (Data Type & Operations)
# 자료형이 다르면 가능한 연산이 다릅니다.
# 같은 연산자라도 자료형에 따라 의미가 달라집니다.
# 그래서 자료형 확인이 중요합니다.

# ----------------------------------------
# [1] 같은 + 연산자, 다른 결과
# ----------------------------------------

# 숫자 + 숫자 = 덧셈
print(10 + 20)       # 30

# 문자열 + 문자열 = 이어붙이기 (연결)
print("10" + "20")   # 1020

# ----------------------------------------
# [2] 같은 * 연산자, 다른 결과
# ----------------------------------------

# 숫자 * 숫자 = 곱셈
print(3 * 4)         # 12

# 문자열 * 숫자 = 반복
print("안녕 " * 3)   # 안녕 안녕 안녕

# ----------------------------------------
# [3] 자료형이 다르면 오류가 발생할 수 있습니다
# ----------------------------------------

# 아래 코드는 오류가 발생합니다
# print("점수: " + 100)   # str + int → TypeError

# 해결 방법: str()로 자료형을 변환
print("점수: " + str(100))  # 점수: 100

# ----------------------------------------
# [4] type()으로 자료형 확인하기
# ----------------------------------------

# 겉보기에 같아 보여도 자료형이 다를 수 있습니다
a = 100
b = "100"

print(a, type(a))   # 100 <class 'int'>
print(b, type(b))   # 100 <class 'str'>

# a + b는 오류! → type()으로 미리 확인하면 오류를 예방할 수 있습니다
